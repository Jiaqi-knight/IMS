imsUnitGaussianQuadraturePoints[ points_ ]:= 
    Sort[ x/. Solve[ 
          Evaluate[ LegendreP[ points, x ]\[Equal]0 ], {x} ], !OrderedQ[  
            N[ { #1, #2 } ]  ]& ];

imsUnitGaussianQuadratureWeights[ points_ ] := Module[
      { gps, f, integr, vars, eqns },
      
      gps = imsUnitGaussianQuadraturePoints[ points ];
      
      f[ 0, 0 ]:= 1;
      f[ 0., 0 ] := 1.;
      f[ x_, n_ ] := x^n;
      
      integr = 
        Integrate[ f[ x, # ], { x, -1, 1 } ]& /@ Range[ 0, points -1 ];
      
      vars = Table[ Unique[c], { Length[ gps ] } ];
      
      eqns = 
        Table[ Plus @@ Thread[ Times[ vars, f[ #, i-1 ]& /@ gps ] ] \[Equal] 
            integr[[ i ]], { i, points } ];
      
      Return[ (vars /. Solve[ eqns, vars ]) ];
      ];

imsUnitGaussianQuadraturePoints[ points_, dim_ ] := 
    Partition[ 
      Flatten[ Outer[ List, 
          Sequence @@ 
            Table[ imsUnitGaussianQuadraturePoints[ points ], { dim } ]  ] ], 
      dim ];

imsUnitGaussianQuadratureWeights[ points_, dim_ ] := 
    Flatten[ Outer[ Times,  
        Sequence @@ 
          Table[ imsUnitGaussianQuadratureWeights[ points ], { dim } ] ] ];



