imsAreaCoordinates[ 
    c:{ {x1_, y1_}, {x2_, y2_}, {x3_, y3_} }, {x_, y_} ] := { 
      (x2*y3 - x3*y2)+(y2-y3)*x+(x3-x2)*y, 
      (x3*y1 - x1*y3)+(y3-y1)*x+(x1-x3)*y, 
      (x1*y2 - x2*y1)+(y1-y2)*x+(x2-x1)*y } / (2*imsArea[ c ])

imsAreaCoordinates[ 
    c:{ {x1_, y1_, z1_}, {x2_, y2_, z2_}, {x3_, y3_, z3_}, {x4_, y4_, 
          z4_} }, {x_, y_, z_} ] := { 
      (-x3 y2+x4 y2+x2 y3-x4 y3-x2 y4+x3 y4) z+x4 y3 z2-x3 y4 z2-x4 y2 z3+
        x2 y4 z3+x3 y2 z4-x2 y3 z4+y (x3 z2-x4 z2-x2 z3+x4 z3+x2 z4-x3 z4)+
        x (-y3 z2+y4 z2+y2 z3-y4 z3-y2 z4+y3 z4),
      (x3 y1-x4 y1-x1 y3+x4 y3+x1 y4-x3 y4) z-x4 y3 z1+x3 y4 z1+x4 y1 z3-
        x1 y4 z3-x3 y1 z4+x1 y3 z4+y (-x3 z1+x4 z1+x1 z3-x4 z3-x1 z4+x3 z4)+
        x (y3 z1-y4 z1-y1 z3+y4 z3+y1 z4-y3 z4),
      (-x2 y1+x4 y1+x1 y2-x4 y2-x1 y4+x2 y4) z+x4 y2 z1-x2 y4 z1-x4 y1 z2+
        x1 y4 z2+x2 y1 z4-x1 y2 z4+y (x2 z1-x4 z1-x1 z2+x4 z2+x1 z4-x2 z4)+
        x (-y2 z1+y4 z1+y1 z2-y4 z2-y1 z4+y2 z4),
      (x2 y1-x3 y1-x1 y2+x3 y2+x1 y3-x2 y3) z-x3 y2 z1+x2 y3 z1+x3 y1 z2-
        x1 y3 z2-x2 y1 z3+x1 y2 z3+y (-x2 z1+x3 z1+x1 z2-x3 z2-x1 z3+x2 z3)+
        x (y2 z1-y3 z1-y1 z2+y3 z2+y1 z3-y2 z3)
      } / ( 6* imsVolume[ c ])

imsAreaShapeFunctions[ c:{ {x1_, y1_}, {x2_, y2_}, {x3_, y3_} }, 
      d:{x_, y_} ] :=imsAreaCoordinates[ c, d ];

imsAreaShapeFunctions[ { {x1_, y1_}, {x2_, y2_}, {x3_, y3_}, {x4_, y4_}, {x5_,
          y5_}, {x6_, y6_} }, {x_, y_} ] := Block[
      {l1, l2, l3 },
      { l1, l2, l3 }=
        imsAreaCoordinates[ { {x1, y1}, {x2, y2}, {x3, y3} }, {x, y} ];
      { (2*l1 -1)l1, (2*l2 -1)l2, (2*l3 -1)l3, 4*l1*l2, 4*l2*l3, 4*l3*l1 }
      ];

imsAreaShapeFunctions[ 
      c:{ {x1_, y1_, z1_}, {x2_, y2_, z2_}, {x3_, y3_, z3_}, {x4_, y4_, 
            z4_} }, d:{x_, y_, z_} ] := imsAreaCoordinates[ c, d ] ;

imsAreaShapeFunctions[ 
      c:{ {x1_, y1_, z1_}, {x2_, y2_, z2_}, {x3_, y3_, z3_}, {x4_, y4_, 
            z4_}, {x5_, y5_, z5_}, {x6_, y6_, z6_}, {x7_, y7_, z7_}, {x8_, 
            y8_, z8_}, {x9_, y9_, z9_}, {x10_, y10_, z10_} }, {x_, y_, z_} ] :=
    Block[
      {l1,l2,l3,l4},
      {l1,l2,l3,l4}= 
        imsAreaCoordinates[ { {x1, y1, z1}, {x2, y2, z2}, {x3, y3, z3}, {x4, 
              y4, z4} }, {x, y, z} ];
      { (2*l1-1)*l1,(2*l2-1)*l2,(2*l3-1)*l3,(2*l4-1)*l4, 4*l1*l2, 4*l2*l3,
        4*l3*l1,4*l2*l4,4*l3*l4,4*l4*l1 }
      ];



