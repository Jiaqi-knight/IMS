imsStationarySolve[ sys_imsSystem, 
        opts___ ] /; !imsParametricStiffnessQ[ sys ]:= Module[
      { lsOpts },
      lsOpts = FilterOptions[ LinearSolve, opts ];
      Return[
        LinearSolve[ First[ imsGetStiffness[ sys ] ], imsGetLoad[ sys ], 
          lsOpts ]  ]
      ];

imsTransientSolve[ sys_imsSystem, f_, { var_, varStart_, varEnd_ }, 
        excitation_:UnitStep, opts___ ] /; 
      imsLinearQ[ sys, f[ var ] ] && !imsParametricQ[ sys ] && 
        imsFirstOrderQ[ sys ]:= Module[
      { ndsOpts, tsOpts, init1, inverse },
      ndsOpts = 
        DeleteCases[ Flatten[ { FilterOptions[ NDSolve, opts ] } ], 
          Rule[ SolveDelayed, _ ] ];
      tsOpts = Options[ imsTransientSolve ];
      init1 = Table[ 0.,{ Length[ imsGetLoad[ sys ] ] } ];
      inverse = Inverse[ First[ imsGetDamping[ sys ] ] ];
      
      NDSolve[
        {
           
          D[ f[ var ],var ] + 
              inverse . First[ imsGetStiffness[ sys ] ] . f[ var ] \[Equal] 
            excitation[ var ] * inverse . imsGetLoad[ sys ],
          f[ varStart ] == init1
          },f, { var, varStart, varEnd }, Join[ tsOpts, ndsOpts ] ]
      ];

