imsHarmonicSolve[ sys_imsSystem,freq_List, ops___] /; !imsParametricQ[ sys ] :=

        Flatten[ # ]& /@
      Which[
        imsFirstOrderQ[ sys ],
        harmonicLoop[ ( 
              I * # * First[ imsGetDamping[sys] ] + 
                First[ imsGetStiffness[ sys ] ] )&,imsGetLoad[ sys ], freq, 
          ops ],
        imsSecondOrderQ[ sys ],
        harmonicLoop[ ( -#^2 * First[ imsGetInertia[ sys ] ] + 
                I * # *  First[ imsGetDamping[sys] ] + 
                First[ imsGetStiffness[ sys ] ] )&,
          imsGetLoad[ sys ], freq, ops ]
        ];

