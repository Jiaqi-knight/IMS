imsInsertInKDTree[ $imsEmptyTree, info_, dim_, index_:1 ] := 
    imsMakeTreeNode[ info, $imsEmptyTree, $imsEmptyTree ];

imsInsertInKDTree[ tree:imsTreeNode[ treeInfo_, left_, right_ ], info_, dim_, 
        index_:1 ] /; Order[ info, treeInfo ] \[Equal]0 := tree;

imsInsertInKDTree[ tree:imsTreeNode[ treeInfo_, left_, right_ ], info_, dim_, 
        index_:1 ] /; info[[ index ]] <  treeInfo[[ index ]] := 
    imsMakeTreeNode[
      treeInfo,
      imsInsertInKDTree[ left, info,dim, Mod[ index+1, dim, 1 ] ],
      right
      ];

imsInsertInKDTree[ tree:imsTreeNode[ treeInfo_, left_, right_ ], info_, 
      dim_,  index_:1 ]:= imsMakeTreeNode[
      treeInfo,
      left,
      imsInsertInKDTree[ right, info, dim, Mod[ index+1, dim, 1 ] ]
      ];

imsRangeSearchKDTree[ {}, lowBound_, highBound_, dim_, index_:1 ] := {};

imsRangeSearchKDTree[ tree:imsTreeNode[ info_, left_, right_ ], lowBound_, 
    highBound_, dim_, index_:1 ] :=
  List[
    If[(And @@ 
          Thread[ lowBound \[LessEqual] info \[LessEqual] highBound ]),{  
        imsGetInformation[ tree ] },{} ],
    If[ (lowBound[[ index ]] \[LessEqual] info[[ index ]]), 
      imsRangeSearchKDTree[ left, lowBound, highBound, dim, 
        Mod[ index +1, dim, 1 ] ],{}],
    If[ (info[[ index ]] < highBound[[ index ]]), 
      imsRangeSearchKDTree[ right, lowBound, highBound, dim, 
        Mod[ index +1, dim, 1 ] ],{}]
    ]

imsNearestPointSearchKDTree[ tree_,pnt_ ]:=Module[
    { nspKD, lowBound={-Infinity,-Infinity}, highBound={Infinity,Infinity}, 
      d=Infinity,d2,nearestPnt,dim },
    
    nspKD[{},index_, dim_] := {};
    
    nspKD[imsTreeNode[ info_, left_, right_ ],index_, dim_]:= 
      CompoundExpression[
        If[(d2= imsDistance[ { info, pnt } ])<d,d=d2;
          nearestPnt=info;{lowBound,highBound}={pnt-d,pnt+d};];
        If[ lowBound[[ index ]]\[LessEqual]info[[ index ]], 
          nspKD[ left, Mod[ index +1,dim, 1 ], dim ] ];
        If[ info[[ index ]] < highBound[[ index ]], 
          nspKD[ right, Mod[ index + 1,dim,1 ], dim ] ]
        ];
    
    dim = Last[ Dimensions[ imsGetInformation[ tree ] ] ];
    nspKD[tree,1, dim];
    Return[ nearestPnt ]
    ]





