imsBalanceKDTree[ $imsEmptyTree,dim_, index_:1 ] := $imsEmptyTree;
imsBalanceKDTree[ pts_, dim_, index_:1 ] := Block[
    { mid = Ceiling[ Length[ pts ] / 2 ], 
      order = Ordering[ pts[[ All,index ]] ] },
    Join[
      pts[[order[[ { mid } ]] ]],
      imsBalanceKDTree[ pts[[ Take[ order, { 1, mid - 1 } ] ]], dim, 
        Mod[ index + 1, dim,1 ] ],
      imsBalanceKDTree[ pts[[ Take[ order, { mid + 1,-1 } ] ]], dim, 
        Mod[ index + 1, dim,1 ] ]
      ]
    ]

