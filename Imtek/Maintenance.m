imsGenerateIMSIndexEntry[ functionName_String, packageName_String, 
      addOnsFunctionPrefix_String, tagName_String ] := CellPrint[
      indexCell[
        functionName, 
        StringDrop[ functionName, 
          Flatten[ StringPosition[ functionName, addOnsFunctionPrefix ] ] ],
         packageName, tagName ]
      ];

imsBuildIndexPackagePair[{{}},{}]:={};
imsBuildIndexPackagePair[ functionNames_List, 
    packageName_String ] := { #, packageName }& /@ functionNames

imsCreateObsoleteFunctionInterface[ functionName_, context_, prefix_:"ims" ] := 
    CompoundExpression[
      obsoleteFunction[ functionName, prefix ];,
      functionName[ args___ ] :=
        ( 
          obsoleteFunctionMessage[ functionName ]; 
          ToExpression[( context <> prefix <>ToString[ functionName ] )][ 
            args ]  ) ];

imsCreateObsoleteFunctionPackageInterface[ oldFunctionName_, 
      newFunctionName_ ] := CompoundExpression[
      obsoletePackageFunction[ oldFunctionName, newFunctionName ];,
      ToExpression[ oldFunctionName ][ args___ ] := (
          obsoletePackageFunctionMessage[ oldFunctionName, newFunctionName ];
          Needs[ Context[ Evaluate[ ToExpression[ newFunctionName ] ] ] ];
          ToExpression[ newFunctionName ][ args ]
          )
      ];

imsCreatePackageIndexEntries[ pathToPackage_ ] := Module[
    { notebookName = pathToPackage,
      thisOpenNotebook,
      notebookContent, 
      rawPackageName,
      thisPackageName,
      rawIndexHits,
      thisIndexEntries
      },
    
    thisOpenNotebook =NotebookOpen[ notebookName ];
    
    thisNotebookContent = NotebookGet[ thisOpenNotebook ];
    
    rawPackageName = Cases[ 
        thisNotebookContent, RowBox[{"BeginPackage","[",___,"]"}], 
        Infinity ];
    thisPackageName = filterPackageName[ rawPackageName ];
    
    If[ isPackageQ[ thisPackageName ],
      rawIndexHits = 
        Cases[ thisNotebookContent, RowBox[{___,"::","usage"}], Infinity  ];
      thisIndexEntries = filterRawHits[ rawIndexHits ];
      ,
      thisIndexEntries = {};
      ];
    
    NotebookClose[ thisOpenNotebook ];
    
    Return[ 
      { Append[ thisIndexEntries, thisPackageName ], thisPackageName } ]
    ]


