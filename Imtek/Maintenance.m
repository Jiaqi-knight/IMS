indexCell[ functionName_, stippedFunctionName_, packageName_, tagName_ ] := 
    Cell[
      TextData[ {
          StringJoin[ functionName, " , " ],
           ButtonBox[
            packageName,ButtonData \[RuleDelayed] tagName,
            ButtonStyle \[Rule] "AddOnsLink"
            ]
          } ],
      "Index",
      CellTags \[Rule] Union[ { functionName, stippedFunctionName } ]
      ];


obsoleteFunctionMessage[ functionName_ ] := 
  Message[ functionName::"imsObsolete", functionName ]

obsoletePackageFunctionMessage[ oldFunctionName_, newFunctionName_ ] := 
  Message[ oldFunctionName::"imsObsoletePackageFunction", oldFunctionName, 
    newFunctionName ]

obsoleteFunction[ functionName_, prefix_ ] :=
  
  functionName::"imsObsolete" = 
    "Function name `1` is obsolete. Try function name " <> 
      prefix <>"`1` instead."

obsoletePackageFunction[ oldFunctionName_, newFunctionName_ ] :=
  
  oldFunctionName::"imsObsoletePackageFunction" = "Function name `1` is obsolete. Use function name `2` instead."
\

filterPackageName[ {} ] := {};
filterPackageName[ { thisPackage_ } ] := Module[
      { contextName, packageName },
      
      contextName = 
        First[ Select[ 
            Flatten[ 
              thisPackage //. 
                RowBox[ this___] \[RuleDelayed] 
                  List[ this ] ], (StringMatchQ[ #, "\"*" ]&), Infinity ] ];
      
      (* "Imtek`blah`packageName` -> packageName *)
      
      packageName = 
        Last[ Flatten[ 
            StringSplit[ StringSplit[ contextName, "\""], "`" ] ] ];
      
      Return[ packageName ];
      ];

filterRawHits[ lst_ ] := filterHit[ # ]& /@ lst;
filterHit[ row_ ] := 
  row /. RowBox[ { functionName_, "::", "usage" } ] \[Rule]   functionName

isPackageQ[ expr_ ] := StringQ[ expr ];



