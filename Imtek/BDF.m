  *)

imsBDFStep[f_,jacobian_,mass_,oldVals_,currentTime_,dt_,coeffs_,
      opts___?OptionQ]:=Module[
      {bk=1/coeffs[[-1]],g,m,tail},
      tail=(Plus@@Times[Most[coeffs],oldVals])/coeffs[[-1]];
      g[un_List]:=
        mass[currentTime+dt,un].(un+tail)-dt*bk*f[currentTime+dt,un];
      m[un_List]:=mass[currentTime+dt,un]-dt*bk*jacobian[currentTime+dt,un];
      imsNonlinearSolve[m,g,oldVals[[-1]],opts]
      ];

(*
