imsBDFSolve[ f_, j_, m_, oldVals_, { tStart_, tEnd_, dt_ }, coeffs_, 
    opts___?OptionQ ] := Block[
    { thisVals = RotateRight[ oldVals ] },
    { Partition[ Join[ #, { tEnd } ], 1 ], 
          ToPackedArray[ 
            FoldList[ 
              imsBDFStep[ f, j, m, thisVals=Rest[ Join[ thisVals, { #1 } ] ];
                   thisVals, #2, dt, coeffs, opts ]&, 
              Last[ oldVals ], # ] ] }&[ Range[ tStart, tEnd-dt, dt ] ] ]

