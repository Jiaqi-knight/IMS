            ToPackedArray[ 
              FoldList[ 
                imsBDFStep[ f, j, thisVals = Rest[ Join[ thisVals, {#1} ] ];
                    thisVals,#2, dt, coeffs, opts ]&, 
                Last[ oldVals ], # ] ] }&[Range[tStart, tEnd-dt, dt]]
