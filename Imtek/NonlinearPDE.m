imsLinearizePDE[ dim_, thisGamma_, thisF_, functions_, vars_ ] := 
    Module[
      { c, alpha, smallGamma, smallBeta, a, f },
      
      c = -Table[ 
            D[ thisGamma[[ l, j ]], ( 
                D[ functions[[ k ]], vars[[ i ]] ] ) ], { l, 
              Length[ functions ] }, { k, Length[ functions ] }, { j, 
              dim }, { i, dim } ];
      alpha = - 
          Table[ D[ thisGamma[[ l, j ]], functions[[ k ]] ], { l, 
              Length[ functions ] }, { k, Length[ functions ] }, { j, 
              dim } ];
      smallGamma =thisGamma;
      smallBeta = -Table[ 
            D[ thisF[[ l ]], ( D[ functions[[ k ]], vars[[ j ]] ] ) ], { l, 
              Length[ functions ] }, { k, Length[ functions ] }, { j, 
              dim } ];
      a = - 
          Table[ D[ thisF[[ l ]], functions[[ k ]] ], { l, 
              Length[ functions ] }, { k, Length[ functions ] } ];
      f= thisF;
      
      Return[ { c, alpha, smallGamma, smallBeta, a, f } ]
      ];



