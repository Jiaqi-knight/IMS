imsReadQDelaunay[ file_ ] := Drop[ Import[ file, "Table"], 1 ]+1;

(* public functions *)
(* *)

imsToQHullInputFile[ coords_ ] := 
    Join[  {{Dimensions[ coords[[1]]][[1]]," "}, {Length[ coords ], " "} }, 
      coords ];

imsDelaunay[ coords_ ] := Module[
      {inputFile, outputFile},
      inputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsInput"] ];
      outputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsOutput"] ];
      Export[ inputFile, imsToQHullInputFile[ coords ],"Table"];
      Run[ 
        "qhull d Qbb i Qt TI \""<>inputFile<>"\" TO \""<>outputFile<>"\"" ];
      Return[ imsReadQDelaunay[ outputFile ] ]; 
      ];

imsConvexHull[ coords_ ] := Module[
      {inputFile, outputFile},
      inputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsInput"] ];
      outputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsOutput"] ];
      Export[ inputFile, imsToQHullInputFile[ coords ],"Table"];
      Run[ "qhull i Qt TI \""<>inputFile<>"\" TO \""<>outputFile<>"\"" ];
      Return[ imsReadQDelaunay[ outputFile ] ]; 
      ];

imsVoronoi[ coords_ ] := Module[
      {inputFile, outputFile},
      inputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsInput"] ];
      outputFile = 
        DirectoryName[ $TemporaryPrefix ]<>ToString[ Unique["imsOutput"] ];
      Export[ inputFile, imsToQHullInputFile[ coords ],"Table"];
      Run[ "qhull v Qbb o TI \""<>inputFile<>"\" TO \""<>outputFile<>"\"" ];
      Return[ Import[ outputFile, "Table" ] ]; 
      ];



