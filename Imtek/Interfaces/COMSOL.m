    Split[ Import[filePath,"Table"],
        Head[ #[[1]] ]===Head[ #2[[1]] ]&][[ {2,4} ]]
  ]

imsReduceComsolData[ { doubleCoordsAndValues_, doubleIncidents_ },dof_:1 ] := 
  Module[
    { doubleCoords, coords, idx,values, i, incidentsToCoords, 
      coordsToNewIncidents, newIncidents  },
    doubleCoords =Drop[#,-dof]&/@doubleCoordsAndValues;
    
    { coords, idx }=imsIndexedUnion[ doubleCoords ];
    
    values = 
      Partition[ 
        doubleCoordsAndValues[[ All, - Range[ dof,1,-1 ] ]][[ 
            Flatten[ idx ] ]], dof ];
    values = 
      doubleCoordsAndValues[[ All, - Range[ dof,1,-1 ] ]][[ 
          Flatten[ idx ] ]];
    
    i=1;
    (incidentsToCoords[i++]=Sequence @@ #)&/@doubleCoords;
    
    i=1;
    (coordsToNewIncidents[Sequence @@ #] = i++ )& /@ coords;
    newIncidents = 
      Map[ coordsToNewIncidents[incidentsToCoords[#]]&, 
        doubleIncidents, {2}];
    Return[ { coords, values, newIncidents } ]
    ]



